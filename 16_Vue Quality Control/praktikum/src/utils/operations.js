export const penjumlahan = (varTambahSatu, varTambahDua) => {
  return varTambahSatu + varTambahDua;
};

export const pengurangan = (varKurangSatu, varKurangDua) => {
  return varKurangSatu - varKurangDua;
};

export const pembagian = (varBagiSatu, varBagiDua) => {
  return varBagiSatu / varBagiDua;
};

export const perkalian = (varKaliSatu, varKaliDua) => {
  return varKaliSatu * varKaliDua;
};

export const rata_rata = (varTotalBilangan, varJumlahSatu, varJumlahDua, varJumlahTiga, jumlahBilangan) => {
  return varJumlahSatu + varJumlahDua + varJumlahTiga, jumlahBilangan / varTotalBilangan;
};

export const pembagian_sisa = (varSisaBagiSatu, varSisaBagiDua) => {
  return varSisaBagiSatu / varSisaBagiDua;
};

export const luas_persegi = (varSisiSatu, varSisiDua) => {
  return varSisiSatu * varSisiDua;
};

export const luas_lingkaran = (varPhiSatu, varJariJariDua) => {
  return varPhiSatu * varJariJariDua * varJariJariDua;
};

export const luas_kubus = (varLuasKubusSatu, varLuasKubusDua) => {
  return varLuasKubusSatu * varLuasKubusDua * varLuasKubusDua;
};

export const volume_kubus = (varVolKubusSatu, varVolKubusDua, varVolKubusTiga) => {
  return varVolKubusSatu * varVolKubusDua * varVolKubusTiga;
};